 <h1>Généralité notions de base réseau</h1>

       <h2>Généralité</h2>
    <p>
      Un réseau informatique est un ensemble d'ordinateur, d'imprimantes de serveurs et autres, reliés entre eux en vue de fournir un certain nombre de services.<br>
      Le but d'un réseau informatique c'est de fournir des services.
      Les services réseaux requièrent deux (2) éléments essentiels: un Client et un Serveur, chacun ayant un logiciel propre.
    </p>

    <p>
      <h2>Client</h2>
          <p>Un client est un ordinateur doté  d'un programme capable de Demander une information puis de la Recevoir.</p>

        <h2>Serveur</h2>
          <p>Un serveur quant à lui, est un ordinateur super puissant qui est capable de Recevoir et d'Interpreter une demande d'informations, puis de Repondre et >Delivrer l'information:c'est l'Application Serveur.
    
          On distingue différents types de serveurs  :
            <ul>
              <li>Les serveurs de fichiers: place centrale ou sont stockés des fichiers de données que l'on met à la disposition des utilisateurs;</li>
              <li>Les serveurs d'impréssions: qui partagent les impréssions</li>
              <li>Les serveurs de messageries</li>
              <li>Le commerce éléctronique</li>
              <li>La vidéo conférence</li>
              <li>Le cloud computing, l'informatique en nuage ou nuagique consiste à exploiter la puissance   de calcul ou de stockage de serveurs informatiques distants par l'intermédiaire d'un réseau, généralement Internet</li>
            </ul>
        <p>
    
        <h2>Matériel réseau </h2>
        <p>Pour relier l'ensemble des ordinateurs, imprimantes, serveurs et autres, il faut des Matériel de connexion (Hubs, Commutateurs) ainsi que des Liaisons/</p>
        <h2>Liaisons</h2>
        <p>Il faut aussi une liaison entre eux pour que l'information puisse circuler le client et le serveur.</p>
        <h2>Protocole </h2>
        <p>Pour fournir un service sur un réseau, le serveur et le client doivent se mettre d'accord sur la façon dont les informations vont etre transmises: Client et Serveur doivent utliser le meme Protocole réseau (TCP/IP,...).</p>
        <h2>La sécurisation du réseau</h2>
        <p>Une fois que le reseau fonctionne, il faut songer à Sécuriser les données et le réseau</p>.
