<h1 class="jumbotron-heading">Classification des réseaux</h1>
  <p>
    Les infrastructures réseau peuvent considérablement varier selon : <ul>
      <li>La taille de la zone couverte;</li>
      <li>Le nombre d'utilisateur connectés;</li>
      <li>Le nombre et les types de service disponibles;</li>
    </ul>
  </p><p>
    Les types de réseau les plus courants d'infrastructures réseau sont :
  </p>
  
    <h2 class="jumbotron-heading"> Un réseau Local (LAN)</h2>
    <p>
      Un réseau local (LAN : Local Area Network) est une infrastructure réseau qui couvre une zone peu étendue. La topologie de ce réseau exige généralement un seul routeur et un ou plusieurs commutateurs. Et peu aussi également aussi comporter des points d'accès sans fil (éventuellement intégrés au routeur) et des téléphones IP. Les LAN relient des périphériques finaux et des utilisateurs dans une zone limitée telle qu'une maison, une école un bureau ou un campus. Le réseau local fournit une bande passante très élevée aux périphériques finaux et aux périphériques intermédiaires internes. En règle générale, un réseau local est administrer par une seule entreprise ou une seule personne. Le contrôle administratif qui gère les stratégies de sécurité et de contrôle d'accès s'applique au niveau du réseau
    </p>
    <p>
      Pour répondre aux besoins des utilisateurs, même les réseaux de petite taille doivent faire l'objet d'une planification et d'une conception. L'un des premiers critères à prendre en compte lors de la mise en oeuvre d'un réseau LAN est le type de périphérique intermédiaires à utiliser pour le fonctionnement du réseau. Plusieurs facteurs déterminent le choix des périphériques intermédiaires.<br>
      LAN sans fil (WLAN) -  infrastructure similaire à un réseau local, mais sans fil. Elle relie des utilisateurs et des terminaux situés dans une zone peu étendue.
    </p>
    <h2 class="jumbotron-heading">Réseau Métropoliatain (MAN)</h2>
    <p>
      Un réseau Métropolitain (MAN), est une infrastructure réseau qui couvre une zone plus vaste qu'un LAN, mais moins étendue qu'un WAN (par exemple, une ville). Les MAN sont généralement gérés par une seule entité, comme une grande entreprise.
    </p>
    <h2>Un réseau Etendue (WAN)</h2>
    <p>
      Un réseau étendu (WAN) est une infrastructure réseau qui couvre une zone étendue. C'est une infrastructure réseau permettant d'accéder à d'autres réseaux sur une vaste zone. Les réseaux étendus sont généralement gérés par des fournisseurs de services ou des fournisseurs d'accès internet (FAI). Les fonctionnalités spécifiques  offertes par les WAN sont les suivantes :<ul>
        <li>Les WAN relient des LAN sur des zones étendues comme plusieurs villes, des Etats, des provinces , des pays ou continents</li>
        <li>Les WAN sont habituellement gérés par plusieurs fournisseurs des services.</li>
        <li>Les réseaux WAN fournissent généralement des liaisons à plus bas débit entre les réseaux locaux</li>
      </ul>
    </p>
    <h2>Internet</h2>
    <p>
      Bien qu'il existe des avantages à utiliser un réseau LAN ou WAN, la plus part des utilisateurs  doivent communiquer avec des ressources situées sur un autre réseau, en dehors du réseau local du domicile, du campus ou de l'entreprise. Cela est éffectué via Internet. Internet est un ensemble mondial de réseaux interconnectés (inter-réseaux ou "internet") qui coopèrent pour échanger des informations en utilisant des normes courantes. Les fils téléphoniques, fibres optiques, transmissions sans fil et liaisons satellites permettent aux utilisateurs d'internet d'échanger des informations sous diverses formes
    </p>
    <p>
      Internet est un ensemble de réseaux dont personne n'est propriétaire. Garantir une communication efficace sur cette infrastructure diverse implique l'application de technologies et de normes cohérentes et communément reconnues, ainsi que la coopération entre de nombreux organismes gouvernementaux. Certains organismes ont été créés pour gérer la structure et la normalisation des protocoles et des processus Internet. Ces organismes incluent Internet Engineering Task Force (IETF), l'Internt Corporation for Assigned Names and Numbers (ICANN) et l'Internet Architecture Board (IAB), entre autres.
    </p>
    <h2>Réseau de stockage SAN</h2>
    <p>
      C'est une infrastructure réseau conçue pour prendre en charge des serveurs de fichiers et pour fournir des fonctionnalités de stockage, de récuparation et de réplication de données. Cette infrastructure comprend les serveurs de hauts gamme, plusieurs baies de disques (appelées blocs) et utilise la technologie d'interconnexion Fibre Channel.
    </p>
    <h2>Intranet</h2>
    <p>
      Le terme Intranet est souvent utilisé pour faire référence à un réseau LAN privé qui appartient à une entreprise ou une administration et auquel peuvent accéder uniquement ses membres, ses employés ou des tierces personnes autorisées. Un intranet est un inter-réseau qui n'est généralement accessuble que depuis le site d'un entreprise.
    </p>
    <p>
      Les entreprises peuvent publier sur un intanet des pages Web sur les événements internes, les politiques de santé et de sécurité, des lettres d'information destinées au personnel et un répertoire téléphonique du personnel. Par exemple, une école peut mettre en place un intranet qui inclut des informations sur les programmes de cours, le cursus en ligne et des forums de discussion. Un intranet permet généralement d'éliminer certains documents papier et d'accélérer les workflows. Un intranet peut être utiliser par le personnel travaillant en dehors du site de l'entrprise grâce à des connexions sécurisées au réseau interne.
    </p>
    <h2>Extranet</h2>
    <p>
      Une entreprise peut utiliser un extranet pour fournir un accès sécurisé aux personnes qui travaillent pour d'autres entreprises, mais qui ont besoin des données de l'entreprise en question. Voici les exemples d'extranets : <ul>
        <li>Une société fournit un accès à des fournisseurs/sous-traitants externes</li>
        <li>Un hôpital propose un système pour que  les médécins puissent planifier les rendez vous avec leurs patients.</li>
        <li>Un bureau local de formation fournit des informations sur le budget et le personnel aux écoles de la région.</li>
      </ul>
    </p>
  
