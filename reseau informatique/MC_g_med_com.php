  <h1 class="jumbotron-heading">Généralité sur les médias de communication</h1>
  <div>
    <p class="cl1">
      La communication à travers un réseau s'éffectue sur un support. Ce support fournit le canal via lequel le méssage se déplace de la source à la destination. Les réseaux modernes utilisent principalement trois types de supports pour interconnecter des périphériques et fournir le chemin par lequel des données peuvent être transmises. Ces supports sont les suivants : 
      <ul>
        <li>Fils métaliques dans les cables : sur des fils métalliques, les données sont codées en impulsion électriques qui correspondent à des modèles spécifiques.</li>
        <li>Fils de verre ou optiques de plastique (câble en fibre optique) : les transmissions par fibre optique s'effectuent via des impulsions de lumière, dans des plages de lumière infrarouges ou visibles. </li>
        <li>Transmission sans fil : dans les transmissions sans fil, des modèles d'ondes électromagnétiques illustrent les différentes valeurs de bit.</li>
      </ul>
    </p>
    <p>
      Les différents types de supports réseau possèdent divers avantages et fonctionnalités. Tous les supports réseau ne possèdent pas les mêmes carctéristiques et ne conviennent pas pour les mêmes objectifs. Les critères de choix d'un support réseau sont :
      <ul>
        <li>La distance sur laquelle les supports peuvent transporter correctement un signal;</li>
        <li>L'environnement dans lequel les supports doivent être installer;</li>
        <li>La quantité de données et le débit de la transmission;</li>
        <li>Le coût des supports et de l'installation;</li>
      </ul>
    </p>
    <p>
      Le chemin emprunté par un message depuis une source jusqu'à une destination peut être aussi simple que la connexion entre deux ordinateurs via un seul câble ou aussi complexe qu'un réseau parcourant le globe terrestre. L'insfrastructure réseau constitue la plateforme qui prend en charge le réseau. Elle fournit le canal stable et fiable à travers lequel nos communications peuvent s'établir. L'infrastructure réseau comprend trois (3) catégories de compsants réseaux : Les périphériques, les multimédia les services.
    </p>
    <p>
      Les périphériques et les supports sont les éléments physiques ou le matériel du réseau. Le matériel correspond souvent aux composants visibles de la plateforme réseau, par exemple un ordinateur portable, un ordinateur de bureau, un commutateur, un routeur, un point d'accès sans fil oû le câblage qui sert à relier les périphériques. Parfois, certains composants ne sont pas visibles. Dans le cas d'un réseau sans fil, les messages sont transmis à travers l'air, à l'aide d'une fréquence radio ou d'ondes infrarouges invisibles.
    </p>
    <p>
      Les composants réseau sont utilisés pour fournir des services et des processus. Les services et les processus sont les programmes sont les programmes de communication, appelés logiciels, qui qont exécutés sur les périphériques réseau. Un service réseau fournit des informations en réponse à une demande. Les services incluent de nombreuses applications réseau courantes utilisées quotidiennement, comme les services d'hébergement de messagerie et les services d'hebergement Web. Les processus fournissent les fonctionalités qui dirigent et déplacent les messages à travers le réseau. Les processus semblent moins équivalent, mais ils sont essentiels au fonctionnement des réseaux.
    </p>
    <p>
      Les périphériques réseaux auxquels les gens sont le plus habitués sont appelés périphériques finaux, ou hôtes. Ces périphériques forment l'interface entre les utilisateurs et le réseau de communication sous-jacent.
      Voici quelques exemples des périphériques finaux :
      <ul>
        <li>Ordinateurs (stations de travail, ordinateurs portables, serveurs de fichiers, serveurs Web) </li>
        <li>Imprimantes réseau</li>
        <li>Téléphone VoIP</li>
        <li>Appareils mobiles (tels que les smartphones, tablettes, PDA, les lecteurs de cartes bancaires et les scanners de codes-barres sans fil) </li>
      </ul>
      Un périphérique hautes est la source ou la destination d'un message transmis sur le réseau
    </p>
    <p>
      Les périphériques intermédiaires relient des périphérques finaux. Ils offrent une connectivité et opèrent en arrière plan pour s'assurer que les données sont transmises sur le réseau. Les périphériques intermédiaires connectent les hôtes individuels au réseau et peuvent connecter plusieurs réseaux individuels afin de former un inter-réseau. Parmi ces périphériques réseau intermédiaires, citons :
      <ul>
        <li>Accès au réseau (commutateurs et points d'accès sans fil) </li>
        <li>Périphériques inter-réseau (routeurs) </li>
        <li>Sécurité (pare-feu) </li>
      </ul>
    </p>
    <p>
      La gestion des données lors de leur passage à travers le réseau constitue également l'un des rôles des périphériques intermédiaires. Ces périphériques utilisent l'adresse d'hôte de destination, avec les informations concernant les interconnexions réseau, de manière à déterminer le chemin que doivent emprunter les messages à travers le réseau.
    </p>
    <p>
      Les processus qui s'exécutent sur les périphériques du réseau intermédiaire remplissent les fonctions suivantes : <ul>
        <li>Régénérer et retransmettre des signaux de données; </li>
        <li>Gérer des informations indiquant les chemins qui existent à travers le réseau et l'inter-réseau;</li>
        <li>Indiquer aux autres périphériques les erreurs et les échecs de communication;</li>
        <li>Diriger des données vers d'autres chemins en cas d'échec de liaison;</li>
        <li>Classer et diriger les messages en fonction des priorités de qualité de service;</li>
        <li>Autoriser ou refuser le flux de données, selon des paramètres de sécurité.</li>
      </ul>
    </p>
